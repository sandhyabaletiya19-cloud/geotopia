  window.searchCountry = function(query) {
    if (!query) return [];
    
    // Normalize query: lowercase, remove spaces, hyphens, dots
    var cleanQuery = query.toLowerCase().replace(/[\s\-\.]/g, '');
    var matches = [];

    for (var i = 0; i < window.COUNTRIES.length; i++) {
      var c = window.COUNTRIES[i];
      var cleanName = c.name.toLowerCase().replace(/[\s\-\.]/g, '');
      var cleanId = c.id.toLowerCase().replace(/[\s\-\.]/g, '');
      
      // Check main name or ID
      var matchFound = cleanName.indexOf(cleanQuery) !== -1 || cleanId.indexOf(cleanQuery) !== -1;
      
      // Check aliases
      if (!matchFound && c.aliases) {
        for (var a = 0; a < c.aliases.length; a++) {
          var cleanAlias = c.aliases[a].toLowerCase().replace(/[\s\-\.]/g, '');
          if (cleanAlias.indexOf(cleanQuery) !== -1) {
            matchFound = true;
            break;
          }
        }
      }
      
      if (matchFound) {
        matches.push(c);
      }
    }
    
    return matches;
  };

