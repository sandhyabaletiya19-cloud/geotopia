    // Get all landforms by country
    getByCountry: function(countryCode) {
        const results = {};
        const categories = [
            'valleys', 'canyons', 'gorges', 'hotDeserts', 'coldDeserts', 
            'coastalDeserts', 'islands', 'archipelagos', 'peninsulas', 
            'caves', 'glacialLandforms', 'volcanicLandforms', 
            'coastalLandforms', 'aeolianLandforms', 'fluvialLandforms'
        ];
        
        categories.forEach(cat => {
            if (this[cat]) {
                results[cat] = this[cat].filter(item => 
                    item.countryCode === countryCode || 
                    item.country.includes(countryCode) ||
                    (item.countries && item.countries.includes(countryCode))
                );
            }
        });
        
        return results;
    },

    // Get all landforms by continent
    getByContinent: function(continent) {
        const results = {};
        const categories = [
            'valleys', 'canyons', 'gorges', 'hotDeserts', 'coldDeserts', 
            'coastalDeserts', 'islands', 'archipelagos', 'peninsulas', 
            'caves', 'glacialLandforms', 'volcanicLandforms', 
            'coastalLandforms', 'aeolianLandforms', 'fluvialLandforms'
        ];
        
        categories.forEach(cat => {
            if (this[cat]) {
                results[cat] = this[cat].filter(item => 
                    item.continent === continent
                );
            }
        });
        
        return results;
    },

    // Get UNESCO World Heritage Sites
    getUNESCOSites: function() {
        const results = [];
        const categories = [
            'valleys', 'canyons', 'gorges', 'hotDeserts', 'coldDeserts', 
            'coastalDeserts', 'islands', 'archipelagos', 'peninsulas', 
            'caves', 'glacialLandforms', 'volcanicLandforms', 
            'coastalLandforms', 'aeolianLandforms', 'fluvialLandforms'
        ];
        
        categories.forEach(cat => {
            if (this[cat]) {
                this[cat].forEach(item => {
                    if (item.unescoStatus) {
                        results.push({
                            category: cat,
                            ...item
                        });
                    }
                });
            }
        });
        
        return results;
    },

    // Search all landforms
    search: function(query) {
        const results = [];
        const searchTerm = query.toLowerCase();
        const categories = [
            'valleys', 'canyons', 'gorges', 'hotDeserts', 'coldDeserts', 
            'coastalDeserts', 'islands', 'archipelagos', 'peninsulas', 
            'caves', 'glacialLandforms', 'volcanicLandforms', 
            'coastalLandforms', 'aeolianLandforms', 'fluvialLandforms'
        ];
        
        categories.forEach(cat => {
            if (this[cat]) {
                this[cat].forEach(item => {
                    if (
                        item.name.toLowerCase().includes(searchTerm) ||
                        item.country.toLowerCase().includes(searchTerm) ||
                        (item.state && item.state.toLowerCase().includes(searchTerm)) ||
                        (item.funFact && item.funFact.toLowerCase().includes(searchTerm))
                    ) {
                        results.push({
                            category: cat,
                            ...item
                        });
                    }
                });
            }
        });
        
        return results;
    },

    // Get statistics
    getStats: function() {
        const stats = {
            totalLandforms: 0,
            byCategory: {},
            byContinent: {},
            unescoSites: 0,
            countries: new Set()
        };
        
        const categories = [
            'valleys', 'canyons', 'gorges', 'hotDeserts', 'coldDeserts', 
            'coastalDeserts', 'islands', 'archipelagos', 'peninsulas', 
            'caves', 'glacialLandforms', 'volcanicLandforms', 
            'coastalLandforms', 'aeolianLandforms', 'fluvialLandforms'
        ];
        
        categories.forEach(cat => {
            if (this[cat]) {
                stats.byCategory[cat] = this[cat].length;
                stats.totalLandforms += this[cat].length;
                
                this[cat].forEach(item => {
                    // Count by continent
                    if (item.continent) {
                        stats.byContinent[item.continent] = (stats.byContinent[item.continent] || 0) + 1;
                    }
                    
                    // Count UNESCO sites
                    if (item.unescoStatus) {
                        stats.unescoSites++;
                    }
                    
                    // Collect countries
                    if (item.countryCode && item.countryCode !== 'MULTI') {
                        stats.countries.add(item.countryCode);
                    }
                });
            }
        });
        
        stats.uniqueCountries = stats.countries.size;
        delete stats.countries;
        
        return stats;
    }
